#include <stdio.h>

/*
 * add - add two integers
 * @a - first integer
 * @b - second integer
 * Return - added final results
 */

int add(int a, int b)
{
	return (a + b);
}


/*
 * sub - subtract two integers
 * @a - first integer
 * @b - second integer
 * Return - subtracted final results
 */

int sub(int a, int b)
{
	return (a - b);
}


/*
 * vi - divide two integers
 * @a - first integer
 * @b - second integer
 * Return - final answer
 */

int div(int a, int b)
{
	return (a / b);
}


/*
 * mul - multiple two integers
 * @a - first integer
 * @b - second integer
 * Return - final results
 */

int mul(int a, int b)
{
	return (a * b);
}


/*
 * mod - mod of two integers
 * @a - first integer
 * @b - second integer
 * Return - mod results
 */

int mod(int a, int b)
{
	return (a % b);
}
